public class IpAddress_template {

    private final String ipAddress;

    public IpAddress_template() {
        ipAddress = "====== Your IP Address =======";
    }

    public String getIpAddress() {
        return ipAddress;
    }

}

/*
사용법
1. 클래스 이름을 "IpAddress_template"에서 "IpAddress"로 변경
2. Constructor에서 ipAddress 변수를 서버의 ip 주소로 초기화
3. 파일 이름을 'IpAddress.java'로 변경
 */